def _selftest_syntax_valid_on_real_topology() -> None:
    """Export agent_brain.spk's actual topology (built via _build_brain()'s
    own logic, replicated here minimally) and confirm the generated Brian2
    script is syntactically valid Python -- the honest ceiling of what can
    be verified without brian2 installed."""
    net = Net(refractory_ms=40)
    S_Work = net.neuron("S_Work", threshold=50, leak=1)
    S_Ambiguous = net.neuron("S_Ambiguous", threshold=50, leak=1)
    Implementer = net.neuron("Implementer", threshold=50, leak=2)
    Reviewer = net.neuron("Reviewer", threshold=50, leak=2)
    S_Work.to(Implementer, weight=1.2)
    S_Ambiguous.inhibits(Implementer, weight=-2.0)
    Implementer.to(Reviewer, weight=1.2)

    code = export_to_brian2(net, model_name="agent_brain")
    try:
        pyast.parse(code)
        syntax_ok = True
        err = None
    except SyntaxError as e:
        syntax_ok = False
        err = str(e)

    print(f"  [{'PASS' if syntax_ok else 'FAIL'}] generated Brian2 script is syntactically "
          f"valid Python{'' if syntax_ok else f' (error: {err})'}")
    if syntax_ok:
        n_lines = len(code.splitlines())
        print(f"    ({n_lines} lines generated for a 4-neuron, 3-synapse subset of "
              f"agent_brain.spk's topology)")
    return syntax_ok


def _selftest_resonators_flagged_not_silently_dropped() -> None:
    """A Resonator neuron must appear in the output as an explicit comment
    explaining it wasn't exported -- not silently vanish."""
    net = Net()
    A = net.neuron("A", threshold=50, leak=0)
    R = net.neuron("R", type="Resonator", freq_hz=440.0, damping=0.1)
    code = export_to_brian2(net, model_name="mixed")
    ok = "R" in code and "NOT exported" in code
    print(f"  [{'PASS' if ok else 'FAIL'}] Resonator neuron is explicitly flagged as not "
          f"exported, not silently dropped from the output")
    return ok


if __name__ == "__main__":
    print("=" * 78)
    print("  PYSPIKE BRIAN2 EXPORT -- neuromorphic-simulator interop")
    print("=" * 78)
    print("  HONESTY NOTE: brian2 is not installed in this environment. Verified:")
    print("  syntactically valid Python, structurally matches Brian2's documented API.")
    print("  NOT verified: actual numerical execution inside a real Brian2 install.")
    print()
    ok1 = _selftest_syntax_valid_on_real_topology()
    ok2 = _selftest_resonators_flagged_not_silently_dropped()
    print()
    if ok1 and ok2:
        print("  Both checks pass, within the honest limits stated above.")
    else:
        print("  Something failed -- see above.")
